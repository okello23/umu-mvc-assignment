      <b style="font-size:30">Issue</b><br>
      <i>Laboratories collect alot of data on patients and their lab results but then
      highly <br>rely on the use of manual systems to generate reports for decision making,<br>
      accounting for testing kits and other Laboratory consummables, a process which
      takes<br> a very long time to accomplish and often times lead to inaccurate reports.<br><br>
      This project is a skeleton on which automation of the generation of some of these
      very <br>much demanded reports can be built. <br>This project considered the most daunting and tideous
      reports (Monthly and Annual reports) <br>which summarizes the total number of tests performed in a given period and<br> further aggregates them into gender.</i>
